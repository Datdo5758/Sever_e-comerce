const jwt = require("jsonwebtoken");
require("dotenv").config();

// Lấy giá trị của Secret Key từ biến môi trường
const secretKeyAdmin = process.env.SECRET_KEY_ADMIN;
const secretKeyClient = process.env.SECRET_KEY;

exports.clients = (req, res, next) => {
  const authHeader = req.get("Authorization");
  if (!authHeader) {
    const error = new Error("Not authenticated.");
    error.statusCode = 401;
    throw error;
  }
  const token = authHeader.split(" ")[1];
  let decodedToken;
  try {
    console.log(123);
    decodedToken = jwt.verify(token, secretKeyClient);
  } catch (err) {
    err.statusCode = 500;
    throw err;
  }
  if (!decodedToken) {
    const error = new Error("Not authenticated.");
    error.statusCode = 401;
    throw error;
  }
  req.userId = decodedToken.userId;
  next();
};

